// The Do/While Loop
# include <iostream>
using namespace std;

int main(){

    int i = 0;

    do{
        // Mostrar o valor de I na tela
        cout << " \n Valor " <<  i << "\n ";
        // Acrescenta o valor de 0 + 1
        i++;
    // Verifica se o valor de i e menor que 5
    }while ( i < 5);

    
    return 0;
}